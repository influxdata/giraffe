const isVoid = (x: any) => x === null || x === undefined

export const getRangeLabel = (min: number, max: number, formatter): string => {
  let label = ''

  if (isVoid(min) || isVoid(max)) {
    label = ''
  } else if (min === max) {
    label = formatter(min)
  } else {
    label = `${formatter(min)} â€“ ${formatter(max)}`
  }

  return label
}
